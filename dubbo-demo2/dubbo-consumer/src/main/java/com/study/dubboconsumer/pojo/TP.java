package com.study.dubboconsumer.pojo;

public class TP {

    private Long tp90;

    private Long tp99;

    public TP() {
    }

    public TP(Long tp90, Long tp99) {
        this.tp90 = tp90;
        this.tp99 = tp99;
    }

    public Long getTp90() {
        return tp90;
    }

    public void setTp90(Long tp90) {
        this.tp90 = tp90;
    }

    public Long getTp99() {
        return tp99;
    }

    public void setTp99(Long tp99) {
        this.tp99 = tp99;
    }
}
